package basics;

import java.util.Scanner;

public class PatternType2 {
    public static void main(String [] args) {
    	int i,j,row;
    	Scanner sc=new Scanner(System.in);
    	
    			row=sc.nextInt();
    			for(i=row;i>1;i--) {
    				for(j=1;j<=i;j++) {
    					System.out.println("* ");
    				}
System.out.println();				
			}
    			}
    			
	}

